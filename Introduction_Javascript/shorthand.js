//add something to the previous value
var price1 =31;
var price2 = 10;

//set a new value
//price1 = 35

price1 = price1 + 10;
console.log(price1); 

//addition to the same variable shorthand
// += means adding some value to the existing value
price1 += 10;
console.log(price1); 
//Substraction to the same variable shorthand
// -= means deduct some value to the existing value
price2 -= 10;
console.log(price2); 
//Multiplication to the same variable shorthand
// *= means multiple some value to the existing value
price2 *= 10;
console.log(price2); 
//Substraction to the same variable shorthand
// /= means division some value to the existing value
price2 /= 10;
console.log(price2); 

//shorthand
// ++ means adding just 1 value from previous value
var happy=100;
happy ++;
console.log(happy);
// -- means deduct just 1 value from previous value
var sad=100;
sad --;
console.log(sad);


